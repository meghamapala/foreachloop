package foreachloop;

public class ForEach_Loop {

	public static void main(String[] args) {
		
		int a [] = {4,6,5,9,8};

		for (int x: a)

		{
			
			System.out.println(x);


		}
		
		String cars [] = {"BMW","Toyota","Benz","Cheavy","Audi"};
		
		for(String c: cars)
			
		{
			System.out.println(c);
		}
	}

}
